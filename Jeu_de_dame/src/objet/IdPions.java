package objet;

public class IdPions {

	
	String charId ;
	int x ;
	int y ;
	
	public IdPions(String charId, int x, int y) {
		super();
		this.charId = charId;
		this.x = x;
		this.y = y;
	}

	public String getCharId() {
		return charId;
	}

	public void setCharId(String charId) {
		this.charId = charId;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
	
}
